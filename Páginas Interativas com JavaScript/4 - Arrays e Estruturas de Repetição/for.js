//Estrutura de uma repetição: for (declaração da variável; condição; incremento) {};

for (let i = 0; i <= 10; i++) {
    console.log(i)
}

/* Função dessa estrutura de repetição:
*
* 1 - A variável "i" se inicia com 0;
* 2 - Enquanto "i" for menor do que 10;
* 3 - será incrementado +1 nessa variável (que passa a ser 2, 3, 4 até chegar a 10).
*
*/ 