// O método especial .pop me retorna o ultimo elemento da array (removendo da array);

const valores = ["1", "2", "3", "4"]
console.log(valores);
console.log(valores.pop()); // Foi retornado para mim, a ultima índice da minha array (que no caso é o valor 4).

valores.pop(); // Foi removido a ultima índice da minha array (que no caso é o valor 3)
console.log(valores); // Foi imprimido os 2 valores que sobraram (no caso: 1 e 2)
